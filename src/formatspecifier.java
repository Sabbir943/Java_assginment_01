public class formatspecifier {
    public static void main(String[] args) {
        boolean b=false;
        int i=12;
        char ch='p';
        float f=67.56f;
        double d=34.6;
        System.out.printf("Boolean=%b\n",b);
        System.out.printf("Integar value=%d\n",i);
        System.out.printf("Charectar value=%c\n",ch);
        System.out.printf("Floating value=%.2f\n",f);
        System.out.printf("Double value=%.2f\n",d);
    }
}
