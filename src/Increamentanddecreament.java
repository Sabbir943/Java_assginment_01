public class Increamentanddecreament {
    public static void main(String[] args) {
        int x=6;
        int y=10;
        x++;
        System.out.println(x);//7
        --x;
        System.out.println(x);// 6
        y++;
        System.out.println(y);//11
        --y;
        System.out.println(y);//10
    

    }
}
